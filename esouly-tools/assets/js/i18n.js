function applyI18n(lang){
    const t = translations[lang] || translations.en;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if(t[key]) el.textContent = t[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        if(t[key]) el.placeholder = t[key];
    });
}

// Auto apply saved lang
(function(){
    const lang = localStorage.getItem('lang') || 'en';
    const labels = {en:'EN',id:'ID',ko:'KO',ja:'JA',zh:'ZH',es:'ES'};
    const flags = {en:'🇬🇧',id:'🇮🇩',ko:'🇰🇷',ja:'🇯🇵',zh:'🇨🇳',es:'🇪🇸'};
    const f = document.getElementById('currentFlag');
    const l = document.getElementById('currentLang');
    if(f && flags[lang]) f.textContent = flags[lang];
    if(l && labels[lang]) l.textContent = labels[lang];
    // Mark active
    document.querySelectorAll('.lang-item').forEach(i => {
        i.classList.toggle('active', i.dataset.lang === lang);
    });
    applyI18n(lang);
})();
